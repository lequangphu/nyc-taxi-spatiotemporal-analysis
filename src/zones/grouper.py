def get_region(zone_id: int) -> str:
    """Get region name for a taxi zone ID."""
    return ZONE_REGION_MAP.get(zone_id, "Unknown")


def add_region_columns(df: pl.DataFrame) -> pl.DataFrame:
    """Add pickup and dropoff region columns."""
    df = df.with_columns(
        [
            pl.col("PULocationID")
            .map_elements(get_region, return_dtype=pl.String)
            .alias("pickup_region"),
            pl.col("DOLocationID")
            .map_elements(get_region, return_dtype=pl.String)
            .alias("dropoff_region"),
        ]
    )
    return df


def trips_by_region(df: pl.DataFrame) -> pl.DataFrame:
    """Get trip counts by pickup region."""
    return (
        df.group_by("pickup_region")
        .agg(
            [
                pl.len().alias("trip_count"),
                pl.col("fare_amount").sum().alias("total_fare"),
                pl.col("fare_amount").mean().alias("avg_fare"),
                pl.col("trip_distance").mean().alias("avg_distance"),
            ]
        )
        .sort("trip_count", descending=True)
    )


def region_to_region_flows(df: pl.DataFrame) -> pl.DataFrame:
    """Get flows between regions."""
    return (
        df.group_by(["pickup_region", "dropoff_region"])
        .agg(
            [
                pl.len().alias("trip_count"),
                pl.col("fare_amount").mean().alias("avg_fare"),
                pl.col("trip_distance").mean().alias("avg_distance"),
            ]
        )
        .sort("trip_count", descending=True)
    )


def region_hourly_patterns(df: pl.DataFrame) -> pl.DataFrame:
    """Get hourly patterns by region."""
    return (
        df.group_by(["pickup_region", "pickup_hour"])
        .agg([pl.len().alias("trip_count")])
        .sort(["pickup_region", "pickup_hour"])
    )
